      SUBROUTINE ROWSEP(A)
C$$$  SUBPROGRAM DOCUMENTATION BLOCK
C                .      .    .                                       .
C SUBPROGRAM:    ROWSEP      SEPARATE NORTHERN AND SOUTHERN LATITUDES.
C   PRGMMR: JOSEPH SELA      ORG: W/NMC23    DATE: 88-04-12
C
C ABSTRACT: SEPARATE NORTHERN AND SOUTHERN LATITUDES OF GAUSSIAN GRID.
C
C PROGRAM HISTORY LOG:
C   88-04-12  JOSEPH SELA
C
C USAGE:    CALL ROWSEP (A)
C   INPUT ARGUMENT LIST:
C     A        - ARRAY OF GAUSSIAN GRID WITH PAIRED
C                NORTHERN AND SOUTHERN LATITUDES.
C                INPUT ARRAY IS OVERWRITTEN BY OUTPUT ARRAY.
C
C   OUTPUT ARGUMENT LIST:
C     A        - ARRAY OF GAUSSIAN GRID WITH SEPARATED
C                NORTHERN AND SOUTHERN LATITUDES.
C
C ATTRIBUTES:
C   LANGUAGE: FORTRAN 200.
C   MACHINE:  CYBER 205.
C
C$$$
      DIMENSION A( 192 , 94 )
      DIMENSION B( 192 ),JDONE( 94 )
      DO 1 K=1, 94
      JDONE(K)=0
 1    CONTINUE
      JSAVE=2
 3    CONTINUE
      IF(JSAVE.LE. 47 ) THEN
        JGET=2*JSAVE-1
      ELSE
        JGET=2*( 94 +1-JSAVE)
      ENDIF
      IF(JGET.EQ.JSAVE) THEN
        JDONE(JSAVE)=1
        GO TO 35
      ENDIF
      DO 5  J=1, 192
      B(J)=A(J,JSAVE)
      A(J,JSAVE)=A(J,JGET)
 5    CONTINUE
      JDONE(JSAVE)=1
10    CONTINUE
      JPUT=JGET
      IF(JPUT.LE. 47 ) THEN
        JGET=2*JPUT-1
      ELSE
        JGET=2*( 94 +1-JPUT)
      ENDIF
      IF(JGET.EQ.JSAVE) GO TO 20
      DO 15 J=1, 192
      A(J,JPUT)=A(J,JGET)
15    CONTINUE
      JDONE(JPUT)=1
      GO TO 10
20    CONTINUE
      DO 30  J=1, 192
      A(J,JPUT)=B(J)
30    CONTINUE
      JDONE(JPUT)=1
35    CONTINUE
      DO 40 K=JSAVE, 94
      IF(JDONE(K).EQ.0) THEN
        JSAVE=K
        GO TO 3
      ENDIF
40    CONTINUE
      RETURN
      END
