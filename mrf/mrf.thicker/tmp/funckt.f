      FUNCTION FUNCKT(THETA,KTYPE)
      PARAMETER(NTYPE=11,NGRID=22)
      COMMON /COMGKT/ KTK(NGRID,NTYPE)
      REAL KTK
      COMMON /COMGDFKT/ B(NTYPE),SATPSI(NTYPE),SATKT(NTYPE),TSAT(NTYPE)
      W = (THETA / TSAT(KTYPE)) * 20. + 1.
      KW = W
      KW = MIN(KW,21)
      KW = MAX(KW,1)
      FUNCKT = KTK(KW,KTYPE)
     &         + (W - KW) * (KTK(KW+1,KTYPE) - KTK(KW,KTYPE))
      RETURN
      END
