      SUBROUTINE PRNTLL(Y,THOUR,ITITL,KMAX)
      DIMENSION Y(KMAX,18)
      CHARACTER*44 ITITL
      PRINT 930
      PRINT 910,THOUR,ITITL
      DO 10 K=1,KMAX
        KK = KMAX +1 -K
        PRINT 920,KK,(Y(KK,LAT), LAT = 1,18)
  10  CONTINUE
      PRINT 930
 910  FORMAT(/,' FCST HOUR=',F4.0,' EQUI-DIST LAT BELT AVE=N.POLE TO',
     1 ' S.POLE ',A44)
 920  FORMAT(I3,2X,18F7.3)
 930  FORMAT(1H ,128(1H- ) )
      RETURN
      END
