      SUBROUTINE MLTFLX(FAC,DUSFC,DVSFC,DTSFC,DQSFC,DLWSFC,ULWSFC,
     1 BENGSH,GFLUX,
     2 DUGWD,DVGWD,PSMEAN)
      DIMENSION DUSFC( 384 , 47 ),DVSFC( 384 , 47 )
      DIMENSION DTSFC( 384 , 47 ),DQSFC( 384 , 47 )
