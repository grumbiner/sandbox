      SUBROUTINE INDDIA
C  SET LEVELS AND INDICES FOR DIAGNOSTICS
C
C  USE KD AS DEFINED BELOW WHEN USING SUBROUTINE ACCDIA
C
C  KD     CNMGDA
C   1     DTLARG
C   2     DTCONV
C   3     DQCONV
C   4     DTSHAL
C   5     DQSHAL
C   6     DTVRDF
C   7     DUVRDF
C   8     DVVRDF
C   9     DQVRDF
C   10    DTHSW
C   11    DTHLW
C   12    CLOUD
C   13    CVCLD
C
C  DIAGNOSTIC INDEXES AND FLAGS
C-DG3 PARAMETER(KDGDA=13)
C-DG3 PARAMETER(KDTLARG=1,KDTCONV=2,KDQCONV=3,KDTSHAL=4,KDQSHAL=5,
C-DG3&          KDTVRDF=6,KDUVRDF=7,KDVVRDF=8,KDQVRDF=9,
C-DG3&          KDTHSW=10,KDTHLW=11,KDTCLD=12,KDTCCV=13)
C-DG3 CHARACTER*8 CNMGDA
C-DG3 COMMON /COMGDC/ CNMGDA(KDGDA)
C-DG3 PARAMETER(NTGDA=92)
C-DG3 PARAMETER(NWGDA=(( 384 * 28 -1)/512+1)*512)
C-DG3 PARAMETER(NRGDA= 47 )
C-DG3 COMMON /COMGDA/ IPUGDA(KDGDA),IBMGDA(KDGDA)
C-DGM COMMON /COMGDD/ GDD(NWGDA*KDGDA*NRGDA)
C
C  SET IDENTIFICATION INDEX
C
C  LARGE-SCALE HEATING
C-DG3 CNMGDA( 1)='DTLARG  '
C-DG3 IPUGDA( 1)=241
C-DG3 IBMGDA( 1)=0
C  DEEP CONVECTIVE HEATING
C-DG3 CNMGDA( 2)='DTCONV  '
C-DG3 IPUGDA( 2)=242
C-DG3 IBMGDA( 2)=0
C  DEEP CONVECTIVE MOISTENING
C-DG3 CNMGDA( 3)='DQCONV  '
C-DG3 IPUGDA( 3)=243
C-DG3 IBMGDA( 3)=0
C  SHALLOW CONVECTIVE HEATING
C-DG3 CNMGDA( 4)='DTSHAL  '
C-DG3 IPUGDA( 4)=244
C-DG3 IBMGDA( 4)=0
C  SHALLOW CONVECTIVE MOISTENING
C-DG3 CNMGDA( 5)='DQSHAL  '
C-DG3 IPUGDA( 5)=245
C-DG3 IBMGDA( 5)=0
C  VERTICAL DIFFUSION OF TEMPERATURE
C-DG3 CNMGDA( 6)='DTVRDF  '
C-DG3 IPUGDA( 6)=246
C-DG3 IBMGDA( 6)=0
C  VERTICAL DIFFUSION OF ZONAL WIND
C-DG3 CNMGDA( 7)='DUVRDF  '
C-DG3 IPUGDA( 7)=247
C-DG3 IBMGDA( 7)=0
C  VERTICAL DIFFUSION OF MERIDIONAL WIND
C-DG3 CNMGDA( 8)='DVVRDF  '
C-DG3 IPUGDA( 8)=248
C-DG3 IBMGDA( 8)=0
C  VERTICAL DIFFUSION OF MOISTURE
C-DG3 CNMGDA( 9)='DQVRDF  '
C-DG3 IPUGDA( 9)=249
C-DG3 IBMGDA( 9)=0
C  SHORT WAVE RADIATION HEATING
C-DG3 CNMGDA(10)='DTHSW   '
C-DG3 IPUGDA(10)=250
C-DG3 IBMGDA(10)=0
C  LONG WAVE RADIATION HEATING
C-DG3 CNMGDA(11)='DTHLW   '
C-DG3 IPUGDA(11)=251
C-DG3 IBMGDA(11)=0
C  CLOUD AMOUNT
C-DG3 CNMGDA(12)='CLOUD   '
C-DG3 IPUGDA(12)=71
C-DG3 IBMGDA(12)=0
C  CONVECTIVE CLOUD AMOUNT
C-DG3 CNMGDA(13)='CVCLD   '
C-DG3 IPUGDA(13)=72
C-DG3 IBMGDA(13)=0
C
      RETURN
      END
