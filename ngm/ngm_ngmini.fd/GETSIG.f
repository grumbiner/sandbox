       SUBROUTINE GETSIG(SIGL)
C$$$  SUBPROGRAM DOCUMENTATION BLOCK
C                .      .    .                                       .
C SUBPROGRAM:    GETSIG      EXTRACT LOG-SIGMA VALUES AT MIDPOINTS
C   PRGMMR: PARRISH          ORG: W/NMC22    DATE: 88-09-08
C
C ABSTRACT: EXTRACT LOG-SIGMA COORDINATE VALUES FOR INTERPOLATION
C   ROUTINE (HERE USE MIDPOINT IN LOG-SIGMA).
C
C PROGRAM HISTORY LOG:
C   88-09-08  PARRISH
C
C USAGE:    CALL GETSIG(SIGL)
C   OUTPUT ARGUMENT LIST:
C     SIGL     - DESIRED COORDINATE VALUES.
C
C ATTRIBUTES:
C   LANGUAGE: FORTRAN200
C   MACHINE:  CYBER
C
C$$$
         include "myparam"
C--------
         REAL SIGIL(INSIGP1),SIGL(INSIG)
         COMMON/FIXSP03/NSIG,NMODES,
     *      DELSIG(INSIG),
     *      HEINVT(INSIG),HEINV(INSIG,INSIG),
     *      HEINVG(INSIG,INSIG),
     *      HPIBINVE(INSIG),HTBINVE(INSIG,INSIG),
     *      HE(INSIG,INSIG),HDEPTHS(INMODES)
C--------
C-------- INITIALIZE ALL INTERNAL VARIABLES IF NOT ALREADY DONE
         IF(NSIG.NE.INSIG) CALL SETSP03
         SIGIL(1)=1.
         DO 100 I=1,NSIG
           SIGIL(I+1)=SIGIL(I)-DELSIG(I)
100      CONTINUE
         SIGIL(NSIG+1)=.1*DELSIG(NSIG)
         DO 200 I=1,NSIG
           SIGL(I)=.5*(LOG(SIGIL(I))+LOG(SIGIL(I+1)))
200      CONTINUE
       RETURN
       END
