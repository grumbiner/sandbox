       SUBROUTINE BALPRNT(NONLOOPB,NUMVERT)
C--------
C-------- PRINT OUT BALANCE STATISTICS
C--------
C$$$  SUBPROGRAM DOCUMENTATION BLOCK
C                .      .    .                                       .
C SUBPROGRAM:    BALPRNT     PRINT BALANCE DIAGNOSTICS
C   PRGMMR: PARRISH          ORG: W/NMC22    DATE: 88-08-08
C
C ABSTRACT: PRINT BALANCE DIAGNOSTICS
C
C PROGRAM HISTORY LOG:
C   88-08-08  PARRISH
C
C USAGE:    CALL BALPRNT(NONLOOPB,NUMVERT)
C   INPUT ARGUMENT LIST:
C     NONLOOPB - NUMBER OF NON-LINEAR ITERATIONS
C     NUMVERT  - NUMBER OF VERT MODES INITIALIZED
C
C ATTRIBUTES:
C   LANGUAGE: FORTRAN200
C   MACHINE:  CYBER
C
C$$$
C--------
         include "myparam"
         COMMON/DIAGBAL/BALN(16,2,9,INUMVERT)
         REAL BALL(16,2,9)
C--------
         JCAP=IJCAPDP
         NONLPIN=MIN(9,NONLOOPB)
         NV=16*2*NONLPIN
         WRITE(6,48)
48       FORMAT (' PLI: INUMCOEF INUMGRID IJCAPDP INLON INLATH',
     *     ' INKLON INKLAT IIORBPL IIORDPL IIORBLP IIORDLP IMCUBIC')
         WRITE(6,50)JCAP
50       FORMAT(' BALANCE STATISTICS FOLLOW FOR JCAP=',I4,/,
     *       '  GROUPED IN WAVE BANDS, 0-9, 10-19, 20-29, . . .')
         DO 900 K=1,NUMVERT
           DO 60 I=1,NV
             BALL(I,1,1)=BALN(I,1,1,K)
             BALL(I,1,1)=MAX(BALL(I,1,1),1.E-25)
             BALL(I,1,1)=LOG10(BALL(I,1,1))
60         CONTINUE
           DO 700 LOOP=1,NONLPIN
             WRITE(6,100)K,LOOP,(BALL(I,1,LOOP),I=1,16)
100          FORMAT(' K,ITER =',2I2,' SLOW BAL=',16F7.2)
             WRITE(6,200)K,LOOP,(BALL(I,2,LOOP),I=1,16)
200          FORMAT(' K,ITER =',2I2,' FAST BAL=',16F7.2)
             WRITE(6,300)
300          FORMAT(1H )
700        CONTINUE
900      CONTINUE
       RETURN
       END
