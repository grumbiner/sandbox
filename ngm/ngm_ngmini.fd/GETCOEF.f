       SUBROUTINE GETCOEF(IV,NV)
C$$$  SUBPROGRAM DOCUMENTATION BLOCK
C                .      .    .                                       .
C SUBPROGRAM:    GETCOEF     ALLOCATE SPACE FOR COEFFICIENTS
C   PRGMMR: PARRISH          ORG: W/NMC22    DATE: 88-07-07
C
C ABSTRACT: RETURN ADDRESSES OF NV USABLE COEF FIELDS IN BLANK COMMON
C
C PROGRAM HISTORY LOG:
C   88-07-07  PARRISH
C   90-07-05  PARRISH (CONVERT TO CRAY)
C
C USAGE:    CALL GETCOEF(IV,NV)
C   INPUT ARGUMENT LIST:
C     NV       - NUMBER OF COEF FIELDS NEEDED
C
C   OUTPUT ARGUMENT LIST:
C     IV       - STARTING INDICES OF EACH COEF FIELD.
C
C ATTRIBUTES:
C   LANGUAGE: FORTRAN77
C   MACHINE:  CRAY
C
C$$$
C--------
         include "myparam"
C--------
         COMMON/FIXSP00/HGRIDS(INUMGRID),HCOEFS(INUMCOEF)
C--------
         INTEGER IV(1)
C--------
         II=0
         DO 100 I=1,INUMCOEF
           IF(HCOEFS(I).EQ.0.) THEN
             II=II+1
             IV(II)=I
             HCOEFS(I)=1.
             IF(II.GE.NV) RETURN
           END IF
100      CONTINUE
C--------
C-------- IF HERE, THEN MUST HAVE RUN OUT OF SPACE, SO NEED TO
C-------- INCREASE NUMCOEF
C--------
C        STOP 'OUT OF SPACE FOR COEFS.  INCREASE NUMCOEF'
         WRITE(6,9887)
9887     FORMAT(' OUT OF SPACE FOR COEFS, INCREASE NUMCOEF')
       RETURN
       END
