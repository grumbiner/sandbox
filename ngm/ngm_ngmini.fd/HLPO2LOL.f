       SUBROUTINE HLPO2LOL(F1,M1,N1,DX1,XPOLE1,YPOLE1,
     *                     F2,M2,N2,
     *                     RK,IK,BK,WORK,
     *        X1WORK,Y1WORK,X2WORK,Y2WORK,RKWORK,IKWORK,RLATH,DX2,
     *        IORDPL)
C$$$  SUBPROGRAM DOCUMENTATION BLOCK
C                .      .    .                                       .
C SUBPROGRAM:    HLPO2LOL    POLA TO LOLA INTERPOLATION ROUTINE
C   PRGMMR: PARRISH          ORG: W/NMC22    DATE: 88-08-08
C
C ABSTRACT: INTERPOLATE FROM POLA TO LOLA GRID.
C
C PROGRAM HISTORY LOG:
C   88-08-08  PARRISH
C
C USAGE:    CALL HLPO2LOL(F1,M1,N1,DX1,XPOLE1,YPOLE1,
C    *                     F2,M2,N2,
C    *                     RK,IK,BK,WORK,
C    *        X1WORK,Y1WORK,X2WORK,Y2WORK,RKWORK,IKWORK,RLATH,DX2,
C    *        IORDPL)
C   INPUT ARGUMENT LIST:
C     F1       - INPUT FIELD TO INTERPOLATE FROM.
C     M1       - INPUT FIELD FIRST DIMENSION
C     N1       - INPUT FIELD SECOND DIMENSION
C     DX1      - INPUT FIELD GRID INCREMENT
C     XPOLE1   - INPUT FIELD
C     YPOLE1   - POLE COORDINATES
C     M2       - OUTPUT FIELD FIRST DIMENSION
C     N2       - OUTPUT FIELD SECOND DIMENSION
C     RLATH    - GAUSSIAN LATITUDES
C     DX2      - LONGITUDE INCREMENT (BOTH IN RADIANS)
C     IORDPL   - ORDER OF INTERPOLATION ( 0 - 3 )
C
C   OUTPUT ARGUMENT LIST:      (INCLUDING WORK ARRAYS)
C     F2       - OUTPUT FIELD OF INTERPOLATED VALUES.
C     RK       - WORK ARRAY
C     IK       - WORK ARRAY
C     BK       - WORK ARRAY
C     WORK     - WORK ARRAY
C     X1WORK   - WORK ARRAY
C     Y1WORK   - WORK ARRAY
C     X2WORK   - WORK ARRAY
C     Y2WORK   - WORK ARRAY
C     RKWORK   - WORK ARRAY
C     IKWORK   - WORK ARRAY
C
C   SUBPROGRAMS CALLED: LOLARXY,TWOPLATE
C
C ATTRIBUTES:
C   LANGUAGE: FORTRAN200
C   MACHINE:  CYBER
C
C$$$
C--------
         include "myparam"
         REAL F1(1),F2(N2,M2),WORK(M2,N2)
         REAL RK(1),X1WORK(1),Y1WORK(1),X2WORK(1)
         REAL Y2WORK(1),RKWORK(1),RLATH(1)
         INTEGER IK(1),IKWORK(1)
         LOGICAL BK(1)
C--------
         NKX=IORDPL+1
         ERAD=6371.22E3
         IORDER=NKX-1
         NF=1
         IZERO=0
         MNN1=M1*N1
         MNN2=M2*N2
         DO 90080 I=1,M1
           X1WORK(I)=DX1*(I-XPOLE1)
90080    CONTINUE
         DO 90084 I=1,N1
           Y1WORK(I)=DX1*(I-YPOLE1)
90084    CONTINUE
c        WRITE(6,8)M1,N1,XPOLE1,YPOLE1,DX1
c8       FORMAT(' IN HLPO2LOL, M1,N1,XPOLE1,YPOLE1,DX1=',
c    *        2I5,3E12.4,' X1,Y1 FOLLOW')
c        WRITE(6,10)(X1WORK(I),I=1,M1)
c        WRITE(6,12)(Y1WORK(I),I=1,N1)
c10      FORMAT(1H ,10E12.4)
c12      FORMAT(1H ,5X,10E12.4)
         CALL LOLARXY(X2WORK,Y2WORK,M2,N2,DX2,
     *               ERAD,RKWORK,IKWORK,RK,RLATH)
         CALL TWOPLATE(F1,M1,N1,X1WORK,Y1WORK,MNN1,
     *             WORK,MNN2,X2WORK,Y2WORK,MNN2,
     *                   IORDER,NF,RK,IK,RKWORK,IKWORK,BK,IZERO)
C--------     TRANSFER RESULT TO F2, PRESERVING VALUES OF F2 ALREADY
C--------      DONE.
         II=0
         DO 200 J=1,N2
           DO 100 I=1,M2
             II=II+1
             IF(BK(II)) F2(J,I)=WORK(I,J)
100        CONTINUE
200      CONTINUE
       RETURN
       END
