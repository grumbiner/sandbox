       SUBROUTINE GETDEPTH(DEPTH,K)
C$$$  SUBPROGRAM DOCUMENTATION BLOCK
C                .      .    .                                       .
C SUBPROGRAM:    GETDEPTH    EXTRACT SCALE DEPTH FOR MODE K
C   PRGMMR: PARRISH          ORG: W/NMC22    DATE: 88-09-08
C
C ABSTRACT: EXTRACT SCALE DEPTH FOR MODE K.
C
C PROGRAM HISTORY LOG:
C   88-09-08  PARRISH
C
C USAGE:    CALL GETDEPTH(DEPTH,K)
C   INPUT ARGUMENT LIST:
C     K        - VERTICAL MODE FOR WHICH DEPTH IS DESIRED.
C
C   OUTPUT ARGUMENT LIST:
C     DEPTH    - DESIRED DEPTH.
C
C ATTRIBUTES:
C   LANGUAGE: FORTRAN200
C   MACHINE:  CYBER
C
C$$$
         include "myparam"
C--------
         REAL DSIG(INSIG)
         COMMON/FIXSP03/NSIG,NMODES,
     *      DELSIG(INSIG),
     *      HEINVT(INSIG),HEINV(INSIG,INSIG),
     *      HEINVG(INSIG,INSIG),
     *      HPIBINVE(INSIG),HTBINVE(INSIG,INSIG),
     *      HE(INSIG,INSIG),HDEPTHS(INMODES)
C--------
C-------- INITIALIZE ALL INTERNAL VARIABLES IF NOT ALREADY DONE
         IF(NSIG.NE.INSIG) CALL SETSP03
         DEPTH=HDEPTHS(K)
       RETURN
       END
