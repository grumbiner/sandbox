       SUBROUTINE SETDSIG(DSIG)
C$$$  SUBPROGRAM DOCUMENTATION BLOCK
C                .      .    .                                       .
C SUBPROGRAM:    SETDSIG     SET SIGMA STRUCTURE
C   PRGMMR: PARRISH          ORG: W/NMC22    DATE: 88-09-08
C
C ABSTRACT: INSERT ARBITRARY VALUE FOR SIGMA-COORDINATE STRUCTURE
C   INTO COMMON/FIXSP03/ TO OVERRIDE DEFAULT EQUAL SPACEING.
C
C PROGRAM HISTORY LOG:
C   88-09-08  PARRISH
C
C USAGE:    CALL SETDSIG(DSIG)
C   INPUT ARGUMENT LIST:
C     DSIG     - DESIRED SIGMA-COORDINATE LAYER THICKNESSES.
C
C ATTRIBUTES:
C   LANGUAGE: FORTRAN200
C   MACHINE:  CYBER
C
C$$$
         include "myparam"
C--------
         REAL DSIG(INSIG)
         COMMON/FIXSP03/NSIG,NMODES,
     *      DELSIG(INSIG),
     *      HEINVT(INSIG),HEINV(INSIG,INSIG),
     *      HEINVG(INSIG,INSIG),
     *      HPIBINVE(INSIG),HTBINVE(INSIG,INSIG),
     *      HE(INSIG,INSIG),HDEPTHS(INMODES)
C--------
         DO 998 I=1,INSIG
           DELSIG(I)=DSIG(I)
998      CONTINUE
       RETURN
       END
