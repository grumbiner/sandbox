      SUBROUTINE ERRSTOP (IOUTUPRT, ERRMSG2)
C$$$  SUBPROGRAM DOCUMENTATION BLOCK
C                .      .    .
C SUBPROGRAM:    ERRSTOP     TERMINATION OF JOB AFTER FATAL ERROR.
C   PRGMMR: JIM HOKE         ORG: W/NMC22    DATE: 85-11-04
C
C ABSTRACT: TERMINATES THE JOB WITH A FATAL ERROR (RETURN CODE
C   OF 8) AFTER WRITING MESSAGES TO THE PRINT FILE
C
C PROGRAM HISTORY LOG:
C   85-11-04  JIM HOKE
C   88-08-04  BRIAN SCHMIDT REVISED THE DOCBLOCK
C   90-07-01  KEN MITCHELL CONVERTED TO CRAY Y-MP
C
C USAGE:  CALL ERRSTOP (IOUTUPRT, ERRMSG2)
C   INPUT ARGUMENT LIST:
C     IOUTUPRT - UNIT NUMBER OF THE PRINT FILE.
C     ERRMSG2  - ERROR MESSAGE OF 2 LINES OF 70 CHARACTERS EACH.
C                IN THE CALLING ROUTINE, ERRMSG2 CAN BE SET UP
C                IN THE FOLLOWING MANNER:
C
C                CHARACTER*35 ERRMSG(4)
C
C                ERRMSG(1) = 'THIS IS A EXAMPLE OF SUBROUTINE ERR'
C                ERRMSG(2) = 'STOP.  THE MESSAGE SHOULD BE 2 LINE'
C                ERRMSG(3) = 'S OF 70 CHARACTERS EACH.           '
C                ERRMSG(4) = '                                   '
C
C                CALL ERRSTOP (6, ERRMSG)
C
C ATTRIBUTES:
C   LANGUAGE:  FORTRAN.
C   MACHINE:   CRAY Y-MP
C
C$$$
C
C
      IMPLICIT REAL (A-H, O-Z)
C
      CHARACTER*35 ERRMSG1(2)
      CHARACTER*1  ERRMSG2(70, 2)
C
C
C
      IERRLEN2 = 70
C
C
C              WRITE A MESSAGE TO THE OUTPUT FILE.
      WRITE (IOUTUPRT,  100) ((ERRMSG2(I,J), I=1,IERRLEN2), J=1,2)
  100 FORMAT ('0', 'THE EXECUTION OF THIS CONTROLLEE WILL BE ',
     1             'ABORTED BECAUSE OF A FATAL ERROR.'/
     2        ' ', 'THE ERROR MESSAGE WRITTEN TO THE DAYFILE IS:  '/
     3        ' ', '                       ', 70A1/
     4        ' ', '                       ', 70A1//)
C
C
      call exit(8)
Cwas  STOP 008
C
      END
