C
      SUBROUTINE PRINTG
C$$$  SUBPROGRAM DOCUMENTATION BLOCK
C                .      .    .                                       .
C SUBPROGRAM:    PRINTG      PRINTS BASIC GRID PARAMETERS
C   PRGMMR: JIM HOKE         ORG: W/NMC22    DATE: 85-03-27
C
C ABSTRACT: PRINTS BASIC GRID PARAMETERS
C
C PROGRAM HISTORY LOG:
C   85-03-27  JIM HOKE
C   88-08-02  BRIAN SCHMIDT ADDED DOCBLOCK
C
C USAGE:    CALL PRINTG
C   INPUT ARGUMENT LIST:
C     NONE
C
C   OUTPUT ARGUMENT LIST:
C     NONE
C
C   OUTPUT FILES:
C     FT06F001 - FOR PRINTOUT OF BASIC GRID PARAMETERS
C
C   SUBPROGRAMS CALLED:
C     UNIQUE:    - CORNER
C     LIBRARY:
C       COMMON   - CONSTN
C
C ATTRIBUTES:
C   LANGUAGE: CYBER FORTRAN 200
C   MACHINE:  CYBER 205
C
C$$$
C
      IMPLICIT REAL (A-H, O-Z)
C
C
      INCLUDE 'parmodel'
C
C
      LOGICAL  BITSEA, BITSNO, BITWVL
C
      COMMON /CONSTN/ IMG( INGRDUSE ), JMG( INGRDUSE ),
     1                IAG( INGRDUS1 ), JAG( INGRDUS1 ),
     2                IBG( INGRDUS1 ), JBG( INGRDUS1 ),
     3                IADDRG( INIADDRS ,  INGRDUSE ),
     4                INDEXHU,  INDEXHV,  INDEXHTH, INDEXHQ,
     5                INDEXH,   INDEXST,  INDEXSQ,  INDEXALB,
     6                INDEXSLP, INDEXPSI, INDEXCD,  INDEXCC,  INDEXGSP,
     7                INDEXRAD, INDEXDLW, INDEXDSW, INDEXCPR, INDEXMA,
     8                INDEXSEC, INDEXZ0, INDEXTSS,  INDEXCG,  INDEXDHQ,
     9                INDEXTSD, KM, NH, NGRDUSE,
     1                SPECS( INSPECS ), DELSIG( IKM ),
     2                POVH( IKM ), PIOVHK( IKM ),
     3                CMUU  ( INGRDUSE ),
     4                XPOLEH( INGRDUSE ), YPOLEH( INGRDUSE ),
     5                XPOLEU( INGRDUSE ), YPOLEU( INGRDUSE ),
     6                XPOLEV( INGRDUSE ), YPOLEV( INGRDUSE ),
     7                DLAMNGM,
     8                BITSEA( IIJMAX ,  INGRDUSE ),
     9                BITSNO( IIJMAX ,  INGRDUSE ),
     1                BITWVL( IIJMAX ,  INGRDUSE )
C
C       ****   END OF INCLUDED COMMON BLOCK CONSTN   ***
C
C
      REARTH = 6371.22E+3
      DEGRAD = 3.141592654E0 / 180.E0
C
      DX45 = (REARTH / (REAL(NH) + 0.5E0)) * (1.0E0+SIN(45.E0*DEGRAD))
      DX60 = (REARTH / (REAL(NH) + 0.5E0)) * (1.0E0+SIN(60.E0*DEGRAD))
C
      PRINT 200
  200 FORMAT ('0', 'GRID', 5X, 'GRID', 6X, 'GRID INCREMENT (KM)',
     1             4X, 'VARIABLE', 5X, 'POLAR LOCATION'/
     2        ' ', 'NAME', 3X, 'DIMENSION', 28X, 'TYPE', 6X,
     3             'XPOLE', 5X, 'YPOLE'/
     4        ' ', 7X, 'IMG', 1X, 'JMG', 6X, '45N', 8X,
     5             '60N'/)
C
      DO 400 NG=1,NGRDUSE
      PRINT 300, NG, IMG(NG), JMG(NG), DX45, DX60, XPOLEH(NG),
     1     YPOLEH(NG), XPOLEU(NG), YPOLEU(NG), XPOLEV(NG), YPOLEV(NG)
  300 FORMAT (' ', I3, I7, I5, -3PF13.5, -3PF11.5, 7X, 'H',
     1             0PF14.5, F10.5/
     2        ' ', 46X, 'U', F14.5, F10.5/
     3        ' ', 46X, 'V', F14.5, F10.5/)
C
      DX45 = 0.5E0 * DX45
      DX60 = 0.5E0 * DX60
C
  400 CONTINUE
C
C     PRINT LATITUDE/LONGITUDE INFORMATION FOR THE
C     CORNERS OF ANY INTERIOR GRID.
C     THE LONGITUDINAL RANGE IS SPECIFIED TO BE -180. TO +180.
      IRANGE = 1
C     THE LATITUDE AND LONGITUDE ARE TO BE PRINTED.
      IPRT = 1
C
      PRINT 24
   24 FORMAT(1H0,'NEGATIVE VALUES DENOTE WEST LONGITUDES:')
C
      DO 50 NG=1,NGRDUSE
C
      PRINT 25, NG
   25 FORMAT(1H0,'GRID', I2, ' CORNERS FOR H POINTS ARE AT:')
      CALL CORNER(1.E0-XPOLEH(NG),1.E0-YPOLEH(NG),CMUU(NG),
     1            DLAMNGM,IRANGE,IPRT,DUMMY,DUMMY)
      CALL CORNER(IMG(NG)-XPOLEH(NG),1.E0-YPOLEH(NG),CMUU(NG),
     1            DLAMNGM,IRANGE,IPRT,DUMMY,DUMMY)
      CALL CORNER(1.E0-XPOLEH(NG),JMG(NG)-YPOLEH(NG),CMUU(NG),
     1            DLAMNGM,IRANGE,IPRT,DUMMY,DUMMY)
      CALL CORNER(IMG(NG)-XPOLEH(NG),JMG(NG)-YPOLEH(NG),CMUU(NG),
     1            DLAMNGM,IRANGE,IPRT,DUMMY,DUMMY)
C
   50 CONTINUE
C
C
      PRINT 100
  100 FORMAT ('0')
C
      RETURN
      END
