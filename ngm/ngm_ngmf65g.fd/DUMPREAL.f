      SUBROUTINE DUMPREAL( X,N )
C...TRANSLATED BY FPP 3.00Z36 11/09/90  15:04:53  
C...SWITCHES: OPTON=I47,OPTOFF=VAE0
C$$$  SUBPROGRAM DOCUMENTATION BLOCK
C                .      .    .                                       .
C SUBPROGRAM:    DUMPREAL    PRINTS N REALS OF ARRAY 'A'
C   PRGMMR: JIM TUCCILLO         ORG: W/NMC4     DATE: 90-06-13
C
C ABSTRACT: PRINTS N REAL NUMBERS IN ARRAY 'X'
C
C PROGRAM HISTORY LOG:
C   90-06-13  JIM TUCCILLO
C
C USAGE:    CALL DUMPREAL (X, N)
C   INPUT ARGUMENT LIST:
C     X        - REAL ARRAY TO BE PRINTED
C     N        - NUMBER OF VALUES OF THE REAL ARRAY 'A'
C                TO BE PRINTED
C
C   OUTPUT FILES:
C     FT06F001 - FOR PRINTOUT
C
C ATTRIBUTES:
C   LANGUAGE: FORTRAN
C   MACHINE:  CRAY Y-MP
C
C$$$
C          ROUTINE TO PRINT THE  N  REAL NUMBERS IN ARRAY  X .
C
      DIMENSION  X(1),Y(5)
C
      PRINT 1
    1 FORMAT(1H0,16X,'N1',94X,'N2')
      N2=5
    2 N1=N2-4
      IF(N1 .GT. N ) RETURN
CMIC$ DO ALL VECTOR SHARED(Y) PRIVATE(M)
      DO 5 M=1,5
      Y(M)=0.E0
    5 CONTINUE
      IF( N2 .GT. N ) N2=N
CMIC$ DO ALL VECTOR SHARED(N2, N1, X, Y) PRIVATE(L)
      DO 10 L=N1,N2
      Y(L+1-N1)=X(L)
   10 CONTINUE
C
      PRINT 3,N1,(Y(L),L=1,5),N2
    3 FORMAT(1H ,2X,I16,5E16.8,I16)
      N2=N2+5
      GO TO 2
C
      END
