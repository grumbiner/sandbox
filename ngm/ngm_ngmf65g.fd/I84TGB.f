      SUBROUTINE I84TGB(IQ,IDGRB)
C$$$  SUBPROGRAM DOCUMENTATION BLOCK
C
C SUBPROGRAM: I84TGB         ON84 Q INTEGER TO GRIB1 PARAMETER
C   PRGMMR: TREADON          ORG: W/NMC20     DATE: 93-11-09
C
C ABSTRACT:
C     THIS ROUTINE CONVERTS A PASSED OFFICE NOTE 84 Q INTEGER
C     (IE, FIELD IDENTIFIER) TO A GRIB 1 PARAMETER (FIELD 
C     IDENTIFIER).  A RETURN VALUE OF 255 INDICATES THAT AN
C     ACCEPTED GRIB 1 PARAMETER DOES NOT EXIST FOR THE GIVEN
C     ON84 Q INTEGER.
C
C PROGRAM HISTORY LOG:
C   93-11-09  TREADON
C
C USAGE:    CALL I84TGB(IQ,IDGRB)
C   INPUT ARGUMENTS:
C     IQ           ON84 Q INTEGER
C   OUTPUT ARGUMENTS:
C     IDGRB        GRIB1 PARAMETER
C
C ATTRIBUTES:
C   LANGUAGE: CRAY FORTRAN
C
C$$$
C     
C     DECLARE VARIABLES
      INTEGER IQ, IDGRB
C     
C*********************************************************************
C     START I84TGB HERE.
C     
C     HEIGHT WRT MEAN SEA LEVEL.
      IF (IQ.EQ.001) IDGRB = 007   ! GEOPOTENTIAL
      IF (IQ.EQ.002) IDGRB = 255   ! PRESSURE ALTITUDE
C     
C     DISTANCE WRT EARTH SURFACE.
      IF (IQ.EQ.006) IDGRB = 008   ! GEOMETRIC DISTANCE ABOVE
      IF (IQ.EQ.007) IDGRB = 008   ! GEOMETRIC DISTANCE BELOW
C     
C     PRESSURE.
      IF (IQ.EQ.008) IDGRB = 001   ! ATMOSPHERIC PRESSURE
      IF (IQ.EQ.009) IDGRB = 003   ! PRESSURE TENDENCY
C     
C     TEMPERATURE.
      IF (IQ.EQ.016) IDGRB = 011   ! ATMOSPHERIC TEMPERATURE
      IF (IQ.EQ.017) IDGRB = 017   ! DEWPOINT TEMPERATURE
      IF (IQ.EQ.018) IDGRB = 018   ! DEWPOINT DEPRESSION
      IF (IQ.EQ.019) IDGRB = 013   ! POTENTIAL TEMPERATURE
      IF (IQ.EQ.020) IDGRB = 015   ! MAXIMUM TEMPERATURE
      IF (IQ.EQ.021) IDGRB = 016   ! MINIMUM TEMPERATURE
      IF (IQ.EQ.022) IDGRB = 085   ! SOIL TEMPERATURE
C     
C     VERTICAL MOTION.
      IF (IQ.EQ.040) IDGRB = 039   ! VERTICAL VELOCITY DP/DT
      IF (IQ.EQ.041) IDGRB = 255   ! NET VERTICAL DISPLACEMENT
      IF (IQ.EQ.042) IDGRB = 040   ! VERTICAL VELOCITY DZ/DT
      IF (IQ.EQ.043) IDGRB = 255   ! OROGRAPHIC COMPONENT DZ/DT
      IF (IQ.EQ.044) IDGRB = 255   ! FRICTIONAL COMPONENT DZ/DT
C     
C     WIND.
      IF (IQ.EQ.048) IDGRB = 033   ! GRID RELATIVE U COMPONENT
      IF (IQ.EQ.049) IDGRB = 034   ! GRID RELATIVE V COMPONENT
      IF (IQ.EQ.050) IDGRB = 032   ! WIND SPEED
      IF (IQ.EQ.051) IDGRB = 255   ! THERMAL WIND SPEED
      IF (IQ.EQ.052) IDGRB = 136   ! VERTICAL SPEED SHEAR
      IF (IQ.EQ.053) IDGRB = 255   ! DIVERGENT GRID RELATIVE U
      IF (IQ.EQ.054) IDGRB = 255   ! DIVERGENT GRID RELATIVE V
      IF (IQ.EQ.055) IDGRB = 031   ! (FROM) WIND DIRECTION (WRT NORTH)
      IF (IQ.EQ.056) IDGRB = 255   ! WESTERLY COMPONENT OF WIND
      IF (IQ.EQ.057) IDGRB = 255   ! SOUTHERLY COMPONENT OF WIND
      IF (IQ.EQ.058) IDGRB = 255   ! RATIO OF SPEEDS
      IF (IQ.EQ.059) IDGRB = 255   ! VECTOR WIND (SPECRAL)
      IF (IQ.EQ.060) IDGRB = 255   ! STEADINESS FACTOR
      IF (IQ.EQ.061) IDGRB = 255   ! WIND GUSTINESS
      IF (IQ.EQ.062) IDGRB = 255   ! DIFFUSIVE U-COMPONENT ACCEL.
      IF (IQ.EQ.063) IDGRB = 255   ! DIFFUSIVE V-COMPONENT ACCEL.
C     
C     FLUID FLOW FUNCTIONS.
      IF (IQ.EQ.072) IDGRB = 042   ! ABSOLUTE VORTICITY
      IF (IQ.EQ.073) IDGRB = 043   ! RELATIVE VORTICITY
      IF (IQ.EQ.074) IDGRB = 044   ! DIVERGENCE
      IF (IQ.EQ.080) IDGRB = 035   ! STREAM FUNCTION
      IF (IQ.EQ.081) IDGRB = 036   ! VELOCITY POTENTIAL
      IF (IQ.EQ.082) IDGRB = 170   ! WESTERLY COMP OF WIND STRESS
      IF (IQ.EQ.083) IDGRB = 171   ! SOUTHERLY COMP OF WIND STRESS
      IF (IQ.EQ.084) IDGRB = 247   ! WESTERLY WIND COMP OF ACCEL BY DIF
      IF (IQ.EQ.085) IDGRB = 248   ! SOUTHERLY WIND COMP OF ACCEL BY DIF
      IF (IQ.EQ.086) IDGRB = 255   ! X COMP OF GRAVITY WAVE DRAG
      IF (IQ.EQ.087) IDGRB = 255   ! Y COMP OF GRAVITY WAVE DRAG
C     
C     MOISTURE.
      IF (IQ.EQ.088) IDGRB = 052   ! RELATIVE HUMIDITY
      IF (IQ.EQ.089) IDGRB = 054   ! PRECIPITABLE WATER
      IF (IQ.EQ.090) IDGRB = 061   ! ACM TOTAL PRECIP
      IF (IQ.EQ.091) IDGRB = 255   ! PROBABILITY OF PRECIP
      IF (IQ.EQ.092) IDGRB = 255   ! PROB OF FROZEN PRECIP
      IF (IQ.EQ.093) IDGRB = 066   ! SNOW DEPTH
      IF (IQ.EQ.094) IDGRB = 063   ! ACM CNVCT PRECIP
      IF (IQ.EQ.095) IDGRB = 051   ! SPECIFIC HUMIDITY
      IF (IQ.EQ.096) IDGRB = 076   ! LIQUID WATER
      IF (IQ.EQ.097) IDGRB = 059   ! RAINFALL RATE
      IF (IQ.EQ.098) IDGRB = 060   ! PROB OF THUNDERSTORM
      IF (IQ.EQ.099) IDGRB = 255   ! CONDITIONAL PROB OF SEVERE LOCAL STORM
      IF (IQ.EQ.100) IDGRB = 255   ! CONDITIONAL PROB OF MAJOR TORNADO OUTBRK
      IF (IQ.EQ.101) IDGRB = 053   ! MIXING RATIO
      IF (IQ.EQ.102) IDGRB = 255   ! UNCONDITIONAL PROB OF SEVERE LOCAL STORM
      IF (IQ.EQ.103) IDGRB = 135   ! MOISTURE CONVERGENCE (DIVERGENCE)
      IF (IQ.EQ.104) IDGRB = 055   ! VAPOR PRESSURE
      IF (IQ.EQ.105) IDGRB = 062   ! ACM NON-CNVCT PRECIP
      IF (IQ.EQ.106) IDGRB = 255   ! ICE ACCRETION RATE
      IF (IQ.EQ.107) IDGRB = 255   ! NON-CNVCT PRECIP RATE
      IF (IQ.EQ.108) IDGRB = 255   ! CNVCT PRECIP RATE
      IF (IQ.EQ.109) IDGRB = 255   ! DEEP CNVCT MOISTURE TENDENCY
      IF (IQ.EQ.110) IDGRB = 255   ! SHALLOW CNVCT MOISTURE TENDENCY
      IF (IQ.EQ.111) IDGRB = 255   ! VERTICAL DIF MOISTURE TENDENCY
C     
C     STABILITY.
      IF (IQ.EQ.112) IDGRB = 131   ! LIFTED INDEX
      IF (IQ.EQ.113) IDGRB = 255   ! TOTAL TOTALS
      IF (IQ.EQ.114) IDGRB = 133   ! K INDEX
      IF (IQ.EQ.115) IDGRB = 255   ! CONVECTIVE INSTABILITY
      IF (IQ.EQ.116) IDGRB = 132   ! 4 LAYER (BEST) LIFTED INDEX
      IF (IQ.EQ.117) IDGRB = 057   ! ACM EVAPORATION
      IF (IQ.EQ.119) IDGRB = 156   ! CONVECTIVE INHIBITION
C     
C     WAVE COMPONENTS.
      IF (IQ.EQ.120) IDGRB = 255   ! LONG WAVE COMP OF GEOPOTENTIAL
      IF (IQ.EQ.121) IDGRB = 255   ! SHORT WAVE COMP OF GEOPOTENTIAL
C     
C     PRESSURE REDUCTIONS.
      IF (IQ.EQ.136) IDGRB = 002   ! MSLP - SHUELL REDUCTION
      IF (IQ.EQ.137) IDGRB = 255   ! MSLP - MAPS REDUCTION
      IF (IQ.EQ.138) IDGRB = 130   ! MSLP - HORIZONTAL REDUCTION
C     
C     MISCELLANEOUS PARAMETERS.
      IF (IQ.EQ.157) IDGRB = 157   ! CONVECTIVE AVAILABLE POTENTIAL ENERGY
      IF (IQ.EQ.158) IDGRB = 158   ! TURBULENT KINETIC ENERGY
      IF (IQ.EQ.159) IDGRB = 077   ! LIFTED CONDENSATION PRESSURE
      IF (IQ.EQ.160) IDGRB = 255   ! DRAG COEFFICIENT
      IF (IQ.EQ.161) IDGRB = 081   ! LAND/SEA FLAG
      IF (IQ.EQ.162) IDGRB = 255   ! K FACTORS
      IF (IQ.EQ.163) IDGRB = 255   ! CONVERSION CONSTS
      IF (IQ.EQ.164) IDGRB = 255   ! SLP SPECIFICATION FROM 700MB HEIGHTS
      IF (IQ.EQ.165) IDGRB = 255   ! REGRESS COEF FOR PROB OF PRECIP
      IF (IQ.EQ.166) IDGRB = 255   ! REGRESS COEF FOR MEAN TEMP
      IF (IQ.EQ.167) IDGRB = 255   ! REGRESS COEF FOR MEAN PRECIP
      IF (IQ.EQ.168) IDGRB = 255   ! ORTHOGONAL PRES FUNC
      IF (IQ.EQ.169) IDGRB = 084   ! ALBEDO
      IF (IQ.EQ.170) IDGRB = 255   ! ENERGY FLUX
      IF (IQ.EQ.171) IDGRB = 255   ! TEMP TENDENCY FROM HEATING
      IF (IQ.EQ.172) IDGRB = 255   ! ENERGY STATISTICS
      IF (IQ.EQ.173) IDGRB = 255   ! TOTAL HEAT FLUX DOWNWARD
      IF (IQ.EQ.174) IDGRB = 255   ! SENSIBLE+EVAPORATIVE HEAT FLUX
      IF (IQ.EQ.175) IDGRB = 255   ! SOLAR HEAT FLUX DOWNWARD
      IF (IQ.EQ.176) IDGRB = 176   ! LATITUDE
      IF (IQ.EQ.177) IDGRB = 177   ! LONGITUDE
      IF (IQ.EQ.178) IDGRB = 255   ! RADAR INTENSITY
      IF (IQ.EQ.179) IDGRB = 255   ! CEILING HEIGHT, TDL
      IF (IQ.EQ.180) IDGRB = 137   ! VISIBILITY
      IF (IQ.EQ.181) IDGRB = 255   ! LIQUID PRECIP (Y/N), TDL
      IF (IQ.EQ.182) IDGRB = 255   ! FREEZING PRECIP (Y/N), TDL
      IF (IQ.EQ.183) IDGRB = 255   ! FROZEN PRECIP (Y/N), TDL
      IF (IQ.EQ.184) IDGRB = 255   ! PROBABILITY
      IF (IQ.EQ.185) IDGRB = 255   ! CONDITIONAL PROBABILITY
      IF (IQ.EQ.186) IDGRB = 255   ! SURFACE FRICTION VELOCITY
      IF (IQ.EQ.187) IDGRB = 255   ! SURFACE FRICTION TEMPERATURE
      IF (IQ.EQ.188) IDGRB = 067   ! MIXING HEIGHT
      IF (IQ.EQ.189) IDGRB = 209   ! NUMBER OF MIXED LAYERS ABOVE SFC
C     
C     RADIATION PARAMETERS.
      IF (IQ.EQ.190) IDGRB = 163   ! DOWNWARD FLUX OF LW RADTN
      IF (IQ.EQ.191) IDGRB = 162   ! UPWARD FLUX OF LW RADTN
      IF (IQ.EQ.192) IDGRB = 161   ! DOWNWARD FLUX OF SW RADTN
      IF (IQ.EQ.193) IDGRB = 160   ! UPWARD FLUX OF SW RADTN
      IF (IQ.EQ.194) IDGRB = 122   ! UPWARD TURBULENT SENSIBLE HEAT FLUX
      IF (IQ.EQ.195) IDGRB = 121   ! UPWARD TURBULENT FLUX OF WATER
      IF (IQ.EQ.196) IDGRB = 255   ! TEMPERATURE TENDENCY FROM LW RADTN
      IF (IQ.EQ.197) IDGRB = 255   ! TEMPERATURE TENDENCY FROM SW RADTN
      IF (IQ.EQ.198) IDGRB = 216   ! TEMPERATURE TENDENCY FROM ALL RADTN
      IF (IQ.EQ.199) IDGRB = 207   ! MOISTURE AVAILABILITY
      IF (IQ.EQ.200) IDGRB = 255   ! RADIANCE
      IF (IQ.EQ.201) IDGRB = 255   ! BRIGHTNESS TEMPERATURE
      IF (IQ.EQ.202) IDGRB = 255   ! TOTAL COLUMN OZONE
      IF (IQ.EQ.203) IDGRB = 255   ! OZONE MIXING RATIO
      IF (IQ.EQ.204) IDGRB = 255   ! RATE OF ABSORPTION OF SW RADTN
C     
C     TENDENCY PARAMETERS.
      IF (IQ.EQ.205) IDGRB = 241   ! TEMP TENDENCY FROM LRGSCL PRECIP
      IF (IQ.EQ.206) IDGRB = 244   ! TEMP TENDENCY FROM SHALLOW CNVCT
      IF (IQ.EQ.207) IDGRB = 242   ! TEMP TENDENCY FROM DEEP CNVCT
      IF (IQ.EQ.208) IDGRB = 246   ! TEMP TENDENCY FROM VERTICAL DIF
      IF (IQ.EQ.209) IDGRB = 255   ! SOIL THERMAL COEFFICENT
C     
C     CLOUD COVER VARIABLES.
      IF (IQ.EQ.210) IDGRB = 213   ! NON-CNVCT CLOUD FRACTION
      IF (IQ.EQ.211) IDGRB = 072   ! CNVCT CLOUD FRACTION
      IF (IQ.EQ.212) IDGRB = 255   ! NON-CNVCT CLOUD BASE PRESSURE
      IF (IQ.EQ.213) IDGRB = 255   ! NON-CNVCT CLOUD TOP PRESSURE
      IF (IQ.EQ.214) IDGRB = 255   ! CNVCT CLOUD BASE PRESSURE
      IF (IQ.EQ.215) IDGRB = 255   ! CNVCT CLOUD TOP PRESSURE
      IF (IQ.EQ.216) IDGRB = 208   ! SFC EXHANGE COEFFICIENT
      IF (IQ.EQ.217) IDGRB = 083   ! SFC ROUGHNESS LENGTH   
      IF (IQ.EQ.218) IDGRB = 082   ! STANDARD DEVIATION OF GROUND HEIGHT
      IF (IQ.EQ.219) IDGRB = 255   ! LOWEST CLOUD LAYER HEIGHT
      IF (IQ.EQ.220) IDGRB = 255   ! HIGHEST CLOUD LAYER HEIGHT
C     
C     OCEANOGRAPHIC VARIABLES.
      IF (IQ.EQ.304) IDGRB = 049   ! U COMP OF CURRENT WRT GRID
      IF (IQ.EQ.305) IDGRB = 050   ! V COMP OF CURRENT WRT GRID
      IF (IQ.EQ.384) IDGRB = 080   ! WATER TEMPERATURE
      IF (IQ.EQ.385) IDGRB = 102   ! HEIGHT OF WIND-DRIVEN WAVES
      IF (IQ.EQ.386) IDGRB = 105   ! HEIGHT OF SEA SWELLS
      IF (IQ.EQ.388) IDGRB = 103   ! PERIOD OF WIND-DRIVEN WAVES
      IF (IQ.EQ.389) IDGRB = 101   ! (FROM) WAVE DIRECTION (WRT NORTH)
      IF (IQ.EQ.390) IDGRB = 106   ! PERIOD OF SEA SWELLS
      IF (IQ.EQ.391) IDGRB = 104   ! (FROM) SWELL DIRECTION (WRT NORTH)
      IF (IQ.EQ.392) IDGRB = 091   ! ICE-FREE WATER SURFACE
      IF (IQ.EQ.400) IDGRB = 100   ! SIG WAVE HEIGHT (SEA AND SWELL)
      IF (IQ.EQ.401) IDGRB = 108   ! PRIMARY WAVE PERIOD
      IF (IQ.EQ.402) IDGRB = 107   ! (FROM) PRIMARY WAVE DIR (WRT N)
      IF (IQ.EQ.403) IDGRB = 110   ! SECONDARY WAVE PERIOD
      IF (IQ.EQ.404) IDGRB = 109   ! (FROM) SECONDARY WAVE DIR (WRT N)
      IF (IQ.EQ.405) IDGRB = 255   ! WHITE CAP COVERAGE
C     
C     END OF ROUTINE.
C
      RETURN
      END
