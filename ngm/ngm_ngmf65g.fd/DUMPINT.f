      SUBROUTINE DUMPINT( I,N  )
C...TRANSLATED BY FPP 3.00Z36 11/09/90  15:04:43  
C...SWITCHES: OPTON=I47,OPTOFF=VAE0
C$$$  SUBPROGRAM DOCUMENTATION BLOCK
C                .      .    .                                       .
C SUBPROGRAM:    DUMPINT     PRINTS N INTEGERS OF ARRAY 'A'
C   PRGMMR: JIM TUCCILLO         ORG: W/NMC4     DATE: 90-06-13
C
C ABSTRACT: PRINTS THE N INTEGERS IN ARRAY 'A'
C
C PROGRAM HISTORY LOG:
C   90-06-13  JIM TUCCILLO
C
C USAGE:    CALL DUMPINT (I, N)
C   INPUT ARGUMENT LIST:
C     I        - INTEGER ARRAY TO BE PRINTED
C     N        - NUMBER OF VALUES OF THE INTEGER ARRAY 'A'
C                TO BE PRINTED
C
C   OUTPUT FILES:
C     FT06F001 - FOR PRINTOUT
C
C ATTRIBUTES:
C   LANGUAGE: FORTRAN
C   MACHINE:  CRAY Y-MP
C
C$$$
C          ROUTINE TO PRINT THE  N  INTEGERS IN ARRAY  I .
C
      INTEGER I(1),J(5)
C
      PRINT 1
    1 FORMAT(1H0,16X,'N1',94X,'N2')
      N2=5
    2 N1=N2-4
      IF(N1 .GT. N ) RETURN
CMIC$ DO ALL VECTOR SHARED(J) PRIVATE(M)
      DO 5 M=1,5
      J(M)=0
    5 CONTINUE
      IF( N2 .GT. N ) N2=N
CMIC$ DO ALL VECTOR SHARED(N2, N1, I, J) PRIVATE(L)
      DO 10 L=N1,N2
      J(L+1-N1)=I(L)
   10 CONTINUE
C
      PRINT 3,N1,(J(L),L=1,5),N2
    3 FORMAT(1H ,2X,7I16)
      N2=N2+5
      GO TO 2
C
      END
