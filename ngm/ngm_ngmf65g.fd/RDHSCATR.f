C
C$$$  SUBPROGRAM DOCUMENTATION BLOCK
C
C SUBPROGRAM:    RDHGATHR    TO SCATTER ACCORDING TO INDEX VECTOR
C   PRGMMR: J TUCCILLO       ORG: W/NMC4     DATE: 90-05-19
C
C ABSTRACT: SCATTER AIN ACCORDING TO IT
C   .
C
C PROGRAM HISTORY LOG:
C   90-05-19  J TUCCILLO
C
C USAGE:  CALL RDHSCATR ( AIN, IT, AOUT, LEN )
C   INPUT ARGUMENT LIST:
C     AIN      - VECTOR TO SCATTER FROM
C     IT       - INDEX VECTOR TO AIN
C     LEN      - LENGTH OF AIN, IT, AOUT
C
C   OUTPUT ARGUMENT LIST:
C     AOUT     - SCATTERED ELEMENTS OF AIN
C
