      DIMENSION AIN ( * ), AOUT ( * ), IT ( * )
CFPP$ PERMUTATION ( IT )
CMIC$ DO ALL VECTOR SHARED(LEN, AIN, IT, AOUT) PRIVATE(I)
      DO 1 I = 1, LEN
         AOUT ( IT ( I ) ) = AIN ( I )
1     CONTINUE
C
      RETURN
      END
