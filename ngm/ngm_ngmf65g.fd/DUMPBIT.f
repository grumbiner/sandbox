      SUBROUTINE DUMPBIT( B, N )
C...TRANSLATED BY FPP 3.00Z36 11/09/90  15:00:31  
C...SWITCHES: OPTON=I47,OPTOFF=VAE0
C$$$  SUBPROGRAM DOCUMENTATION BLOCK
C                .      .    .                                       .
C SUBPROGRAM:    DUMPBIT     PRINTS N VALUES OF A BIT VECTOR
C   PRGMMR: JIM TUCCILLO         ORG: W/NMC4     DATE: 90-06-13
C
C ABSTRACT: PRINTS THE N 1'S OR 0'S IN THE BIT VECTOR 'B'.
C
C PROGRAM HISTORY LOG:
C   90-06-13  JIM TUCCILLO
C
C USAGE:    CALL DUMPBIT (B, N)
C   INPUT ARGUMENT LIST:
C     B        - BIT VECTOR THAT IS TO BE PRINTED
C     N        - NUMBER OF VALUES OF THE BIT VECTOR 'B' TO BE PRINTED
C
C   OUTPUT FILES:
C     FT06F001 - FOR PRINTOUT OF 1'S AND 0'S OF BIT VECTOR 'B'
C
C ATTRIBUTES:
C   LANGUAGE: FORTRAN
C   MACHINE:  CRAY Y-MP
C
C$$$
C
      LOGICAL B(1)
      INTEGER I(50)
C
      PRINT 1
    1 FORMAT(1H0,12X,'N1',17X,'(10)',16X,'(20)',16X,'(30)',
     1 16X,'(40)',16X,'(50)',6X,'N2')
C       NHDG WILL COUNT FOR PRINTING COLUMN HEADINGS.
      NHDG=0
      N2=50
    2 N1=N2-49
      IF(N1 .GT. N ) GO TO 10
CMIC$ DO ALL VECTOR SHARED(I) PRIVATE(IQ2W6E)
      DO 88890 IQ2W6E=1,50
         I(IQ2W6E)=-1
88890 CONTINUE
      IF( N2 .GT. N ) N2=N
      LEN=N2-N1+1
CMIC$ DO ALL VECTOR SHARED(LEN, N1, I, B) PRIVATE(IQ2W6E)
      DO 88900 IQ2W6E = 1, LEN
         I(IQ2W6E) = 0
         IF (B(N1+IQ2W6E-1)) I(IQ2W6E) = 1
88900 CONTINUE
C     END WHERE
      PRINT 3, N1, (I(M),M=1,50), N2
    3 FORMAT(1H ,2X,I12,50I2,I12 )
      NHDG=NHDG+1
      IF( NHDG .LT. 10) GO TO 5
      PRINT 1
      NHDG=0
    5 CONTINUE
      N2=N2+50
      GO TO 2
C
   10 PRINT 1
C
      RETURN
      END
