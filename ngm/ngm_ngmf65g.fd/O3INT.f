               IF (L5S) RBAR1U = RDATA(K)
               IF (L5S) RBAR(KK) = RBAR1U
77060       CONTINUE
         ENDIF
   99 CONTINUE
CMIC$ END PARALLEL
C     CALCULATE TOTAL OZONE
      O3RD=0.E0
      DO 89 KK=1,80
   89 O3RD=O3RD+RDATA(KK)*(PH(KK+1)-PH(KK))
      O3RD=O3RD+RDATA(81)*(P(81)-PH(81))
      O3RD=O3RD/980.E0
      O3TOT=0.E0
      DO 88 KK=1,N
   88 O3TOT=O3TOT+RBAR(KK)*(PHALF(KK+1)-PHALF(KK))
      O3TOT=O3TOT/980.E0
C     UNITS ARE MICROGRAMS/CM**2
      O3DU=O3TOT/2.144E0
C     O3DU UNITS ARE DOBSON UNITS (10**-3 ATM-CM)
CMIC$ DO ALL VECTOR SHARED(L, RBAR, DDUO3N) PRIVATE(KK)
      DO 23 KK=1,N
   23 DDUO3N(L,KK)=RBAR(KK)*.01E0
   33 CONTINUE
C***END OF LATITUDE LOOP
C
C***CREATE 5 DEG OZONE QUANTITIES BY LINEAR INTERPOLATION OF
C      10 DEG VALUES
CMIC$ PARALLEL SHARED(IPLACE,O35DEG,O3O3,DDUO3N)PRIVATE(JJ,KEN,KK,L)
CMIC$ DO PARALLEL
      DO 1060 KK=1,N
        DO 1061 L=1,19
          O35DEG(2*L-1,KK)=DDUO3N(L,KK)
1061    CONTINUE
        DO 1062 L=1,18
          O35DEG(2*L,KK)=0.5E0*(DDUO3N(L,KK)+DDUO3N(L+1,KK))
1062    CONTINUE
1060  CONTINUE
C
CMIC$ DO PARALLEL
      DO 77061 JJ = 1, 592
         O3O3(JJ,1,IPLACE) = O35DEG(JJ,1)
77061 CONTINUE
CMIC$ END PARALLEL
C
 3000 CONTINUE
C
C***END OF LOOP OVER CASES
C
      RETURN
