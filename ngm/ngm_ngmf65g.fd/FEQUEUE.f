      SUBROUTINE FEQUEUE ( FEHOLD, FENAME, IRUNMARK )
C
C$$$  SUBPROGRAM DOCUMENTATION BLOCK
C
C SUBPROGRAM:    FEQUEUE     SUBMIT JOB TO FRONT END INPUT QUEUE
C   PRGMMR: J TUCCILLO       ORG: W/NMC4     DATE: 90-05-19
C
C ABSTRACT:    SUBMITS JOB TO FRONT END INPUT QUEUE
C   .
C
C PROGRAM HISTORY LOG:
C   90-05-20  J TUCCILLO
C
C USAGE:  CALL FEQUEUE ( FEHOLD, FENAME, IRUNMARK )
C   INPUT ARGUMENT LIST:
C     FEHOLD   - FILE TO HOLD JOB TO SUBMIT TO FRONT END INPUT QUEUE
C     FENAME   - NAME OF FRONT END DATASET HOLDING THE JOB
C     IRUNMARK - RUNMARKERRONT END DATASET HOLDING THE JOB
C
C   OUTPUT ARGUMENT LIST:
C     NONE
C
C   SUBPROGRAMS CALLED:
C     LIBRARY:
C              ISHELL
C
C ATTRIBUTES:
C   LANGUAGE: FORTRAN
C   MACHINE:  CRAY Y-MP
C
C$$$
C
      CHARACTER*6 FEHOLD
      CHARACTER*39 FENAME
      CHARACTER*39 FENAMELE
      CHARACTER*1 FENAMEL(39)
      CHARACTER*100 FETCH
      CHARACTER*1 FETCHA(100)
      CHARACTER*25 DISPOSE
      CHARACTER*46  AFORMAT
      CHARACTER*3 AFORM
C
      EQUIVALENCE ( FETCH, FETCHA )
      EQUIVALENCE ( FENAMEL, FENAMELE )
C
      IF ( IRUNMARK .NE. 2 ) RETURN
      IF ( 1 .EQ. 1 ) RETURN
      DO 100 I = 1, 100
         FETCHA(I) = ' '
100   CONTINUE
      FENAMELE = FENAME
      ICNUM = 0
      DO 1 I = 1, 39
         IF ( FENAMEL(I) .EQ. ' ' ) GOTO 2
         ICNUM = ICNUM + 1
1     CONTINUE
2     CONTINUE
      IF ( ICNUM .EQ. 0 ) THEN
         PRINT 300
300      FORMAT(' **** NO FILE TO FETCH AND DISPOSE TO FE')
         RETURN
      END IF
      WRITE(AFORM,497) ICNUM
497   FORMAT('A',I2)
      WRITE(AFORMAT,499)  AFORM
      WRITE(FETCH,AFORMAT)  FEHOLD, FENAME
      ISTAT =ISHELL(FETCH)
      PRINT 498, ISTAT, FEHOLD
498   FORMAT(' ISTAT = ',2X,I4,' FOR DATASET =',2X,A6)
499   FORMAT('("fetch ",A6," -mvs -t''dsn=",',A3,
     1        ',",disp=shr''")')
C
C     DISPOSE TO FRONT-END
C
      WRITE(DISPOSE,900) FEHOLD
900   FORMAT("dispose ",A6," -mvs -d IN")
      ISTAT = ISHELL(DISPOSE)
      PRINT 498, ISTAT, FEHOLD
C
C
      RETURN
      END
