      SUBROUTINE RESTARTW (IOUTURST, IOUTUPRT, DATANAME, SPECS, VBL,
     1                     BITSEA, BITSNO, BITWVL)
C
C$$$  SUBPROGRAM DOCUMENTATION BLOCK
C
C SUBPROGRAM:    RESTARTW    WRITES RESTART FILE
C   PRGMMR: J TUCCILLO       ORG: W/NMC4     DATE: 90-06-19
C
C ABSTRACT:    WRITES RESTART FILE WITH A MAXIMUM RECORD LENGTH OF
C              INUMREC ELEMENTS
C
C PROGRAM HISTORY LOG:
C   90-06-19  J TUCCILLO
C
C USAGE: CALL RESTARTW (IOUTURST, IOUTUPRT, DATANAME, SPECS, VBL,
C                       BITSEA, BITSNO, BITWVL)
C   INPUT ARGUMENTS:
C     IOUTURST  -  UNIT NUMBER OF RESTART FILE
C     IOUTUPRT  -  UNIT NUMBER OF OUTPUT FILE
C     SPECS     -  SPECIFICATION FILE
C     VBL       -  CONTAINS NGM MODEL FIELDS
C     BITSEA    -  CONTAINS UNFROZEN WATER LOGICAL MAP
C     BITSNO    -  CONTAINS SNOW COVER LOGICAL MAP
C     BITWVL    -  CONTAINS WATER VS. LAND LOGICAL MAP
C
C   SUBROUTINES CALLED:
C     UNIQUE    -  OUTOPEN
C
C ATTRIBUTES:
C   LANGUAGE: FORTRAN
C   MACHINE:  CRAY Y-MP
C
C$$$
C
      INCLUDE 'parmodel'
C
      DIMENSION VBL    ( INVBL ),
     *          SPECS  ( INSPECS ),
     *          BITSEA ( IIJMAX * INGRDUSE ),
     *          BITSNO ( IIJMAX * INGRDUSE ),
     *          BITWVL ( IIJMAX * INGRDUSE )
C
      LOGICAL BITSEA, BITSNO, BITWVL
C
      CHARACTER*6 DATANAME
C
C================================================================
C
      GMT0     = SPECS(2)
      DAY0     = SPECS(3)
      AMONTH0  = SPECS(4)
      CENYEAR0 = SPECS(5)
      IRUNMARK = NINT(SPECS(235))
      IOUTUPRT = NINT(SPECS(271))
      IVERSION = NINT(SPECS(231))
C
      CALL OUTOPEN(DATANAME, CENYEAR0, AMONTH0, DAY0, GMT0, IRUNMARK,
     *             IVERSION, IOUTUPRT, IOUTURST, 0)
C
      ILEFT = MOD ( INSPECS, INUMREC )
      ILOOP = INSPECS / INUMREC
      IST   = 1
      ISP   = INUMREC
      DO 1 II = 1, ILOOP
         WRITE ( IOUTURST ) (SPECS(I),I=IST,ISP)
         IST = ISP + 1
         ISP = ISP + INUMREC
1     CONTINUE
      IF ( ILEFT .GT. 0 ) THEN
         ISP = INSPECS
         WRITE ( IOUTURST ) (SPECS(I),I=IST,ISP)
      END IF
C
      ILEFT = MOD ( INVBL, INUMREC )
      ILOOP = INVBL / INUMREC
      IST   =     1
      ISP   = INUMREC
      DO 2 II = 1, ILOOP
        WRITE ( IOUTURST ) (VBL(I), I=IST, ISP)
        IST = ISP + 1
        ISP = ISP + INUMREC
2     CONTINUE
      IF( ILEFT .NE. 0) THEN
        ISP = INVBL
        WRITE ( IOUTURST ) (VBL(I), I=IST, ISP)
      END IF
C
C     SAME VALUES FOR ALL LOGICAL MASKS
C
      ILEN = IIJMAX * INGRDUSE
      ILEFT = MOD ( ILEN, INUMREC )
      ILOOP = ILEN / INUMREC
C
C
      IST   =     1
      ISP   = INUMREC
      DO 3 II = 1, ILOOP
        WRITE ( IOUTURST ) (BITSEA(I), I=IST, ISP)
        IST = ISP + 1
        ISP = ISP + INUMREC
3     CONTINUE
      IF( ILEFT .NE. 0) THEN
        ISP = ILEN
        WRITE ( IOUTURST ) (BITSEA(I), I=IST, ISP)
      END IF
C
      IST   =     1
      ISP   = INUMREC
      DO 4 II = 1, ILOOP
        WRITE ( IOUTURST ) (BITSNO(I), I=IST, ISP)
        IST = ISP + 1
        ISP = ISP + INUMREC
4     CONTINUE
      IF( ILEFT .NE. 0) THEN
        ISP = ILEN
        WRITE ( IOUTURST ) (BITSNO(I), I=IST, ISP)
      END IF
C
      IST   =     1
      ISP   = INUMREC
      DO 5 II = 1, ILOOP
        WRITE ( IOUTURST ) (BITWVL(I), I=IST, ISP)
        IST = ISP + 1
        ISP = ISP + INUMREC
5     CONTINUE
      IF( ILEFT .NE. 0) THEN
        ISP = ILEN
        WRITE ( IOUTURST ) (BITWVL(I), I=IST, ISP)
      END IF
C
C
      RETURN
      END
